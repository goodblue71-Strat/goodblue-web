export async function POST(req: NextRequest) {
  try {
    const deck = await req.json();

    const meta = deck.meta || {};
    const s2   = deck.Slide2_ExecutiveSummary         || {};
    const s3   = deck.Slide3_MarketLandscape          || {};
    const s4   = deck.Slide4_HeadToHead               || {};
    const s5   = deck.Slide5_Competitor_DeepDive_SWOT || {};
    const s6   = deck.Slide6_StrategicWhiteSpace      || {};
    const s7   = deck.Slide7_NextSteps                || {};
    const s8   = deck.Slide8_Sources_Methodology      || {};

    const pres = new pptxgen();
    pres.layout = "LAYOUT_16x9";
    pres.title  = `GoodBlue Competitive Analysis – ${meta.company || ""}`;
    pres.author = "GoodBlue.ai";

    addTitleSlide(pres, meta);
    addExecSummarySlide(pres, s2);
    addMarketLandscapeSlide(pres, s3);
    addHeadToHeadSlide(pres, s4);
    addDeepDiveSlide(pres, s5);
    addWhiteSpaceSlide(pres, s6);
    addNextStepsSlide(pres, s7);
    addSourcesSlide(pres, s8);

    const buffer = await pres.write({ outputType: "nodebuffer" }) as Buffer;
    const uint8Array = new Uint8Array(buffer);

    return new NextResponse(uint8Array, {
      status: 200,
      headers: {
        "Content-Type": "application/vnd.openxmlformats-officedocument.presentationml.presentation",
        "Content-Disposition": `attachment; filename="GoodBlue_${meta.company || "Analysis"}_Competitive_Analysis.pptx"`,
      },
    });
  } catch (err) {
    console.error("PPTX generation error:", err);
    return NextResponse.json({ error: "Failed to generate PPTX" }, { status: 500 });
  }
}
