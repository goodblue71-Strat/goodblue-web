function addMarketLandscapeSlide(pres: any, slide3: any) {
  const slide = pres.addSlide();
  slide.background = { color: C.offWhite };
  addSlideHeader(slide, pres, "Market Landscape & Positioning", `${slide3.Axes?.XAxis || "Market Reach"} vs ${slide3.Axes?.YAxis || "Capabilities"}`);
  const qx = 0.4, qy = 1.75, qw = 5.8, qh = 3.6;
  const midX = qx + qw / 2, midY = qy + qh / 2;
  slide.addShape(pres.shapes.RECTANGLE, { x: qx, y: qy, w: qw, h: qh, fill: { color: C.white }, line: { color: C.lightGray, width: 1 } });
  slide.addShape(pres.shapes.LINE, { x: midX, y: qy, w: 0, h: qh, line: { color: C.lightGray, width: 1 } });
  slide.addShape(pres.shapes.LINE, { x: qx, y: midY, w: qw, h: 0, line: { color: C.lightGray, width: 1 } });
  const quadColors: any = { Leaders: C.quadLeader, Challengers: C.quadChall, Niche: C.quadNiche, Emerging: C.quadEmerg };
  const quadPositions: any = {
    Leaders:     { x: midX + 0.1, y: qy + 0.1 },
    Challengers: { x: qx + 0.1,   y: qy + 0.1 },
    Niche:       { x: qx + 0.1,   y: midY + 0.1 },
    Emerging:    { x: midX + 0.1, y: midY + 0.1 },
  };
  Object.entries(quadPositions).forEach(([label, pos]: [string, any]) => {
    slide.addText(label, { x: pos.x, y: pos.y, w: 2.5, h: 0.3, fontSize: 9, bold: true, color: quadColors[label] || C.gray, fontFace: "Calibri", margin: 0 });
  });
  slide.addText(slide3.Axes?.XAxis || "Market Reach & Adoption", { x: qx, y: qy + qh + 0.08, w: qw, h: 0.25, fontSize: 9, color: C.gray, align: "center", fontFace: "Calibri", margin: 0 });
  const points = slide3.PlotPoints || [];
  points.forEach((pt: any) => {
    if (typeof pt.x !== "number" || typeof pt.y !== "number") return;
    const clampedX = Math.min(Math.max(pt.x, 0.5), 9.5);
    const clampedY = Math.min(Math.max(pt.y, 0.5), 9.5);
    const px = qx + (clampedX / 10) * qw - 0.18;
    const py = qy + ((10 - clampedY) / 10) * qh - 0.18;
    const dotColor = quadColors[pt.category] || C.blue;
    slide.addShape(pres.shapes.OVAL, { x: px, y: py, w: 0.36, h: 0.36, fill: { color: dotColor }, line: { color: dotColor } });
    slide.addText(pt.name || "", { x: px - 0.3, y: py + 0.35, w: 1.2, h: 0.25, fontSize: 7.5, color: C.darkText, align: "center", fontFace: "Calibri", margin: 0 });
  });
  const obs = slide3.Observations || [];
  slide.addText("Key Observations", { x: 6.4, y: 1.75, w: 3.3, h: 0.3, fontSize: 11, bold: true, color: C.navy, fontFace: "Calibri", margin: 0 });
  obs.slice(0, 5).forEach((o: string, i: number) => {
    const oy = 2.15 + i * 0.65;
    slide.addShape(pres.shapes.RECTANGLE, { x: 6.4, y: oy, w: 0.04, h: 0.42, fill: { color: C.blue }, line: { color: C.blue } });
    slide.addText(o, { x: 6.55, y: oy, w: 3.1, h: 0.5, fontSize: 9, color: C.darkText, fontFace: "Calibri", valign: "top", margin: 0 });
  });
}

function addHeadToHeadSlide(pres: any, slide4: any) {
  const slide = pres.addSlide();
  slide.background = { color: C.offWhite };
  addSlideHeader(slide, pres, "Head-to-Head Comparison");
  const table = slide4.Table || {};
  const cols = table.Columns || [];
  const rows = table.Rows || [];
  if (cols.length === 0 || rows.length === 0) return;
  const companies = cols.slice(1);
  const colW = [2.2, ...companies.map(() => (7.4 / companies.length))];
  const headerRow = cols.map((col: string, i: number) => ({
    text: col,
    options: { fill: { color: i === 0 ? C.navy : C.blue }, color: C.white, bold: true, fontSize: 10, fontFace: "Calibri", align: i === 0 ? "left" : "center", valign: "middle" }
  }));
  const tableData = [headerRow];
  rows.slice(0, 5).forEach((row: any, ri: number) => {
    const rowBg = ri % 2 === 0 ? C.white : "F1F5F9";
    const dataRow = cols.map((col: string, ci: number) => {
      if (ci === 0) return { text: row.Capability || "", options: { fill: { color: rowBg }, bold: true, fontSize: 9.5, fontFace: "Calibri", color: C.darkText, align: "left", valign: "middle" } };
      const val = row.Values?.[col] || "-";
      return { text: val, options: { fill: { color: rowBg }, fontSize: 8.5, fontFace: "Calibri", color: strengthColor(val), align: "center", valign: "middle" } };
    });
    tableData.push(dataRow);
  });
  slide.addTable(tableData, { x: 0.4, y: 1.75, w: 9.2, h: 3.3, colW, border: { pt: 0.5, color: C.lightGray }, rowH: 3.3 / (rows.slice(0, 5).length + 1) });
  if (slide4.Takeaway) {
    slide.addShape(pres.shapes.RECTANGLE, { x: 0.4, y: 5.15, w: 9.2, h: 0.35, fill: { color: C.lightBlue }, line: { color: C.lightBlue } });
    slide.addText(`► ${slide4.Takeaway}`, { x: 0.55, y: 5.17, w: 9, h: 0.3, fontSize: 9.5, color: C.navy, fontFace: "Calibri", italic: true, margin: 0 });
  }
}

